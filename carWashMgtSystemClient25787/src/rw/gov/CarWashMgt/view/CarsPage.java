    private void addRowsDataToCarsTable(){
        try{
            tableModel.setRowCount(0);
            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",81);
            CarsService service = (CarsService)theRegistry.lookup("cars");
            List<Cars> cars = service.findAll();
            for(Cars theCars : cars){
                tableModel.addRow(new Object[]{
                    theCars.getId(),
                    theCars.getPlateNo(),
                    theCars.getCarBrand(),
                    theCars.getMgnYear(),
                    theCars.getOwner(),
                    theCars.getFuelType(),
                    theCars.getTransmissionType(),
                    
                });
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        
    }


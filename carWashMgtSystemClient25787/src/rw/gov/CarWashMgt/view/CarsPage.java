    private void carRegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_carRegisterBtnActionPerformed
        // TODO add your handling code here:
        try{
            if(carPlateNumberTxt.getText().trim().isEmpty() || carBrandTxt.getText().trim().isEmpty() || carMgnYearTxt.getText().trim().isEmpty() || carOwnerTxt.getText().trim() .isEmpty() || carFuelCombo.getSelectedItem().equals("Select") || carTransmissionCombo.getSelectedItem().equals("Select")){
                JOptionPane.showMessageDialog(null, "All Fields are required", "Missing", JOptionPane.ERROR_MESSAGE);
            }else{
                Cars theCars = new Cars();
                theCars.setPlateNo(carPlateNumberTxt.getText());
                theCars.setCarBrand(carBrandTxt.getText());
                int year = Integer.parseInt(carMgnYearTxt.getText());
                theCars.setMgnYear(year);
                theCars.setOwner(carOwnerTxt.getText());
                theCars.setFuelType(carFuelCombo.getSelectedItem().toString());
                theCars.setTransmissionType(carTransmissionCombo.getSelectedItem().toString());
                CarWash dao = new CarWash();
                if(dao.checkCarExist(theCars)==0){
                if(carPlateNumberTxt.getText().matches("^[A-Z]{3}[0-9]{3}[A-Z]{1}") && carBrandTxt.getText().length()>=2 && carOwnerTxt.getText().length()>=2){
                    int a = JOptionPane.showConfirmDialog(null, "Register?","APPROVE",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.WARNING_MESSAGE);
                    if(a==0){
                        Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",81);
                        CarsService service = (CarsService)theRegistry.lookup("cars");
                        Cars cars = service.saveCars(theCars);
                        if(cars!=null){
                            JOptionPane.showMessageDialog(this, "Data Saved Successful");
                        }else{
                            JOptionPane.showMessageDialog(this, "Data Not Saved");
                        }
                    }if(a==1){
                        carPlateNumberTxt.setText("");
                        carBrandTxt.setText("");
                        carMgnYearTxt.setText("");
                        carOwnerTxt.setText("");
                        carFuelCombo.setSelectedIndex(0);
                        carTransmissionCombo.setSelectedIndex(0);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Enter Values with Accurate Lenght", "ERROR",JOptionPane.ERROR_MESSAGE);
                }
                }else{
                    JOptionPane.showMessageDialog(this, "Car Already Exist");
                }

            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_carRegisterBtnActionPerformed

